public class ComparisonOperator {
    public static void main(String[] args) {
        int x = 1;
        int y = 1;

        // = is for assignment 
        // == comparison operator
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        // System.out.println(x <= y);

    }
}
